        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include an <strong><code>Authorization</code></strong> header with the value <strong><code>"Bearer Bearer {YOUR_SANCTUM_TOKEN}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>Authentification via <b>Laravel Sanctum</b>. Obtenez un token via <code>POST /api/v1/auth/login</code> puis passez-le en header <code>Authorization: Bearer {token}</code>.</p>

